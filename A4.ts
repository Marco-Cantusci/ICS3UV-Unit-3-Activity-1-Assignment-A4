// store 56.9 as number1
const number1: number = 56.9

// store 89.7 as number2
const number2: number = 89.7

// store 90.2 as number3
const number3: number = 90.2

// add numbers
const addNumbers: number = number1 + number2 + number3

// calculation of average
const average: number = addNumbers / 3

// print average
console.log("The average of", number1, ",", number2, ",", number3, "is", average)

